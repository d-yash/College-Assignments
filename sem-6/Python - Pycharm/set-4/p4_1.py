l1 = [24, 1, 5, 30, 56]
l1.sort()
print(f"{l1}")
print(f"\nl1.index(56) --> {l1.index(56)}")
print(f"l1.pop() --> {l1.pop()}")
l1.insert(4, 11)
print(f"l1.insert(4, 11) --> {l1}")
l1.append(24)
print(f"l1.append(24) --> {l1}")
print(f"l1.count(24) --> {l1.count(24)}")
l2 = l1.copy()
print(f"l2 = l1.copy() --> {l2}")
l2.reverse()        #reverse order
print(f"l2.reverse()  --> {l2}")
l2.remove(1)
print(f"l2.remove(1) --> {l2}")
l2.clear()
print(f"l2.clear() --> {l2}")
l1.extend([-1, -2, -3])
print(f"l1.extend([-1, -2, -3]) --> {l1}")